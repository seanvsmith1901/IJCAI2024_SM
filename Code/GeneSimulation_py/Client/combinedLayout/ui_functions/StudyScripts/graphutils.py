def get_rot(theta):
    return np.array([
        [np.cos(theta), -np.sin(theta)],
        [np.sin(theta), np.cos(theta)]
    ])


def find_endpts(c1, c2, theta, radius1, radius2):
    R = get_rot(theta)
    R_ = get_rot(-theta)

    r1 = c2 - c1
    r1 = r1 / np.linalg.norm(r1) * radius1
    
    r2 = c1 - c2
    r2 = r2 / np.linalg.norm(r2) * radius2

    return R @ r1 + c1, R_ @ r1 + c1, R @ r2 + c2, R_ @ r2 + c2